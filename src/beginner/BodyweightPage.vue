           <p class="collapse" id="viewdetailsw3">
Here we cover three main areas of your back and combine them into one workout.  First we target the upper back including the traps, rhomboids and posterior delts.  We can target this area of our back using an exercise we call the Back Widow.  This upper back exercise is a bodyweight back killer that allows for a self spot if needed by incorporating a bit more push from the feet on the floor.  If you don’t need the help however, you want to maintain a push through the elbows to power your back off the floor and help build your upper back muscles. <br>

